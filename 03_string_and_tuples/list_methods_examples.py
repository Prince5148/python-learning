#append method
list=["prince","tech","AI","software"]
list.append("technology")
print(list)                               # output ['prince', 'tech', 'AI', 'software', 'technology']

#insert method
list=["bike","car","truck"]
list.insert(0,"bus")
print(list)                                #output ['bus', 'bike', 'car', 'truck']

#remove method
list=["leptop","computer","tv"]
list.remove("tv")
print(list)                                #output ['leptop', 'computer']

#pop method
list=["watch","phone","ipad"]
list.pop()
print(list)                                #output ['watch', 'phone']

#sort method
list=[5,4,3,2,1]
list.sort()
print(list)                                #output [1, 2, 3, 4, 5]  