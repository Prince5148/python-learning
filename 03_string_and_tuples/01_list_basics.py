#Creating lists
list=["information technology","computer engineering","civil engineering"]
print("list of branch: ",list)        #output list of branch:  ['information technology', 'computer engineering', 'civil engineering']

#accessing elements
list=["apple","banana","mango"]
print(list[0])                        #output apple
print(list[1])                        #output banana
list[2]="pineapple"  
print(list)                           #output ['apple', 'banana', 'pineapple'] list is mutable              

#Slicing
list=["BMW","MAHINDRA","HYUNDAI","PORSCHE","FERRARY"]
print(list[0:2])                      #output ['BMW', 'MAHINDRA']
print(list[1:3])                      #output ['MAHINDRA', 'HYUNDAI']



