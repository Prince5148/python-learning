# Operators and Type Conversion

x = "10"
y = 5

# Type conversion
x = int(x)

# Arithmetic Operators
print("Addition:", x + y)
print("Subtraction:", x - y)
print("Multiplication:", x * y)
print("Division:", x / y)

# Comparison Operators
print("Is x greater than y?", x > y)
print("Is x equal to y?", x == y)
